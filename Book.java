public class Book {
  String judul;
  String kategori;
  String penulis;
  String penerbit;
  int tahun;
  
  public Book(String judul, String kategori, String penulis, String penerbit, int tahun){
    this.judul = judul;
    this.kategori = kategori;
    this.penulis = penulis;
    this.penerbit = penerbit;
    this.tahun = tahun;
  }
}